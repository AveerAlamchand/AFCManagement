    public void showClubPlayerContents(){
        try{
            DefaultTableModel tblModel = (DefaultTableModel)ClubTbl.getModel();
            tblModel.setRowCount(0);//clear the table

            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/AFCManagement","root","Alamchand@123");
            Statement st = con.createStatement();

            String sql = "SELECT * FROM FOOTBALLCLUB";
            ResultSet rs = st.executeQuery(sql);

            while(rs.next()){
                String id = rs.getString("ClubID");
                String name = rs.getString("ClubName");
                String country = rs.getString("Country");
                String FoundingY = String.valueOf(rs.getInt("FoundingYear"));

                String tbData [] = {id,name,country,FoundingY};
                tblModel.addRow(tbData);
            }

            con.close();
        }catch(Exception e){
            System.out.println(e.getMessage());
        }
    }
