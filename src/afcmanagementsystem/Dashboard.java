    private void ClubReportBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ClubReportBtnActionPerformed
        
    String sql = "{ CALL Report1(?, ?) }";

    try (
        Connection conn = DriverManager.getConnection(
            "jdbc:mysql://localhost:3306/AFCManagement","root", "Alamchand@123");
        CallableStatement cs = conn.prepareCall(sql)
    ) {
        // 1) Bind the IN parameters
        cs.setInt   (1, 2025);
        cs.setString(2, "January");

        // 2) Execute and grab the ResultSet
        try (ResultSet rs = cs.executeQuery()) {
            // 3) Build a TableModel on the fly
            DefaultTableModel model = (DefaultTableModel) ClubReportTbl.getModel();
            //DefaultTableModel model = (DefaultTableModel) PlayerFATbl.getModel();

            // 4) Populate rows
            while (rs.next()) {
                model.addRow(new Object[]{
                    rs.getString("ClubName"),
                    rs.getDouble("TotalSpent"),
                    rs.getDouble("LargestPurchase"),
                    rs.getString("TopPlayerFirst"),
                    rs.getString("TopPlayerLast")
                });
            }

            // 5) Set the model on your JTable
            ClubReportTbl.setModel(model);
        }

    } catch (SQLException ex) {
        JOptionPane.showMessageDialog(this,
            "Failed to load report:\n" + ex.getMessage(),
            "Database Error",
            JOptionPane.ERROR_MESSAGE);
    }
        
        ClubReportPanel.setVisible(true);
        ClubsPanel.setVisible(false);
    }//GEN-LAST:event_ClubReportBtnActionPerformed

