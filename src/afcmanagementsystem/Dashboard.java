    private void ClubEarningsBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ClubEarningsBtnActionPerformed

        String sql = "{ CALL Top5NetIncomePerCapacity() }";

        try (
            Connection conn = DriverManager.getConnection(
                "jdbc:mysql://localhost:3306/AFCManagement", "root", "Alamchand@123");
            CallableStatement cs = conn.prepareCall(sql)
        ) {
            try (ResultSet rs = cs.executeQuery()) {
                DefaultTableModel model = (DefaultTableModel) ClubEarningTbl.getModel();
                model.setRowCount(0); // Clear any existing rows

                while (rs.next()) {
                    model.addRow(new Object[] {
                        rs.getString("ClubName"),
                        rs.getString("StadiumName"),
                        rs.getInt("StadiumCapacity"),
                        rs.getDouble("IncomePerCapacity"),
                        rs.getDouble("TotalIncome")
                    });
                }

                ClubEarningTbl.setModel(model);
            }

        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this,
                "Failed to load club earnings report:\n" + ex.getMessage(),
                "Database Error",
                JOptionPane.ERROR_MESSAGE);
        }

        // Show the relevant panel
        EarningsReportPanel.setVisible(true);
        BusinessPanel.setVisible(false);

    }//GEN-LAST:event_ClubEarningsBtnActionPerformed

